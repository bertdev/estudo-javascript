//crie uma função que ira receber dois valores(dividendo e divisor) e deve imprimir o resultado da divisão e o resto

const imprimirCocienteEResto = (dividendo, divisor) => {
  console.log(`o resultado é = ${dividendo / divisor}`);
  console.log(`o resto é = ${dividendo % divisor}`);
};

imprimirCocienteEResto(15, 5);
imprimirCocienteEResto(15, 2);
