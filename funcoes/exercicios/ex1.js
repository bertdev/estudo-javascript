//criar uma função que recebe 2 parametros e imprime no console as quarto opreações com eles
function imprimeTodasOperacoes(x, y) {
  console.log(`${x} + ${y} = ${x + y}`);
  console.log(`${x} - ${y} = ${x - y}`);
  console.log(`${x} * ${y} = ${x * y}`);
  console.log(`${x} / ${y} = ${x / y}`);
};

imprimeTodasOperacoes(5,5);
