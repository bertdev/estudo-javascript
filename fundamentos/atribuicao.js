//Operador de atribuição e algumas variações

//atribuição comum
const a = 7;
let b = 3;

//atribuição com operações aritimeticas 

//atribuição aditiva
b += a; //b = b + a
console.log(b);

//atribuição subtrativa
b -= 4; //b = b - 4
console.log(b);

//atribuição multiplicativa
b *= 2; //b = b * 2
console.log(b);

//atribuição divisiva 
b /= 6; //b = b / 2
console.log(b);

//atribuição exponencial(calculo de potenciação)
b **= 2; //b = b ** 2
console.log(b);

//atribuição modular(calculo de módulo: resto da divisão)
b %= 2; //b = b % 2
console.log(b);
