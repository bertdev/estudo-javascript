//operadores aritmeticos também são considerados operadores binarios pois atuam em 2 operandos 

const [a, b, c, d] = [3, 5, 1, 15];

//operador de adição +
const soma = a + b + c + d;
console.log(soma); 

//operador de subtração - 
const subtracao = d - b;
console.log(subtracao);

//operador de multiplicação 
const multiplicacao = a * b;
console.log(multiplicacao)

//operador de exponenciação **
const exponenciacao = b ** 2;
console.log(exponenciacao);

//operador de divisão /
const divisao = d / a;
console.log(divisao);

//operador de módulo(resto da divisão) %
const modulo = d % a;
console.log(modulo);
